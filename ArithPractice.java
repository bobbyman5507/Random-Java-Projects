public class ArithPractice //main class
{
	public static void main(String[] args) //main method
	{
		System.out.println("4/3*(5/3*4)="+ 4/3*(5/3*4)); //quick maths
		System.out.println("(5%6)/2="+(5%6)/2);
		System.out.println("5%(6/2)="+ 5%(6/2));
		System.out.println("10.0/2.0*3="+10.0/2.0*3);
		//define and create variables [Start]
			int a=3;
			int b=4;
			int c=4;
			int d=5;
			int e=10;
		//define and create variables [End]
		System.out.println("e%d="+e%d); //more quick maths
		System.out.println("(e%c)/(b*2+(24%a))="+(e % c) / (b * 2 + (24 % a)));
		System.out.println("(a*b*c%d/e)*3="+(a * b * c % d / e) * 3);
		System.out.println("1%(e/a*b%e)="+1%(e / a * b % e));
	}

}
